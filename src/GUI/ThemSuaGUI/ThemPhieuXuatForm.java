    private boolean check_inputTable(String maPX, String maSach){
        if (maPX.equals("")){
            error_mess = "Mã Phiếu Xuất trống!!!";
            return false;
        }
        if (maSach.equals("")){
            error_mess = "Mã Sách trống!!!";
            return false;
        }
        if (dp.check_maPhieuXuat(maPX)!=true){
            error_mess = "Mã Phiếu Xuất nhập sai!!!";
            return false;
        }
        if (dp.check_maSach(maSach)!=true){
            error_mess = "Mã Sách nhập sai!!!";
            return false;
        }
        return true;
    }

    private boolean check_input(String maNV, String ngayXuat){
        if (maNV.equals("")){
            error_mess = "Mã Nhân Viên trống!!!";
            return false;
        }

        if (ngayXuat.equals("")){
            error_mess = "Ngày Xuất trống!!!";
            return false;
        }
        if (dp.check_maQuanLy(maNV)!=true){
            error_mess = "Mã Nhân Viên nhập sai!!!";
            return false;
        }
        if (dp.check_ngaythangnam(ngayXuat)!=true){
            error_mess = "Ngày Xuất nhập sai!!!";
            return false;
        }

        return true;
    }

